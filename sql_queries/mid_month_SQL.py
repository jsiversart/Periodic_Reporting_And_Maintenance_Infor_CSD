CORE_ISSUE_CHECK_SQL = """WITH inquiry AS (
  SELECT 
    i.prod, 
    i.prodtype, 
    i.statustype, 
    i.impliedcoreprod, 
    i.dirtycoreprod, 
    i.descrip_1,
    CASE 
      WHEN i.prod LIKE 'IC%' THEN SUBSTR(i.prod, 3)
      WHEN i.prod LIKE 'DC%' THEN SUBSTR(i.prod, 3)
      ELSE i.prod
    END AS base_prod,
    CASE 
      WHEN i.prod LIKE 'IC%' THEN 'IC'
      WHEN i.prod LIKE 'DC%' THEN 'DC'
      ELSE 'BASE'
    END AS prod_category
  FROM default.icsp i
  WHERE 
  	cono = 1 AND
  	prod not in ('TNK50','ICTNK50','DCTNK50','TNKEXCHANGE','ICTNKEXCHANGE','DCTNKEXCHANGE','MCT','ICMCT','DCMCT','40','IC40','DC40','20','IC20','DC20','B','ICB','DCB','MC','ICMC','DCMC','N20','ICN20','DCN20','MC20','ICMC20','DCMC20','60','IC60','DC60') AND
    (upper(prodtype) in ('R','I','C') 
    OR ((prod LIKE 'IC%' OR prod LIKE 'DC%') AND statustype = 'L') 
    OR i.prod IN (
      SELECT SUBSTR(prod, 3)
      FROM default.icsp
      WHERE 
        (prod LIKE 'IC%' OR prod LIKE 'DC%')
        AND statustype = 'L'
    ))
),
-- Optional: Pivot issue flags for each base_prod
issues AS (
  SELECT 
    base_prod,
    -- EXAMPLE ISSUES:
    -- IC-specific logic
    MAX(CASE WHEN prod_category = 'IC' AND upper(prodtype) != 'I' THEN 1 ELSE 0 END) AS ic_prodtype_wrong,
    MAX(CASE WHEN prod_category = 'DC' AND upper(prodtype) != 'C' THEN 1 ELSE 0 END) AS dc_prodtype_wrong,
    MAX(CASE WHEN prod_category = 'BASE' AND upper(prodtype) != 'R' THEN 1 ELSE 0 END) AS base_prodtype_wrong,
    MAX(CASE WHEN prod_category = 'BASE' AND (impliedcoreprod IS NULL OR TRIM(impliedcoreprod) = '') THEN 1 ELSE 0 END) AS missing_ic_core,
    MAX(CASE WHEN prod_category = 'BASE' AND (dirtycoreprod IS NULL OR TRIM(dirtycoreprod) = '') THEN 1 ELSE 0 END) AS missing_dc_core,
    MAX(CASE WHEN prod_category = 'BASE' AND descrip_1 not in ('CONTROL - ADD 60.00 CORE','CONTROL - ADD 55.00 CORE')  THEN 1 ELSE 0 END) AS base_description_issue
  FROM inquiry
  GROUP BY base_prod
)
SELECT 
  base_prod,
  ic_prodtype_wrong,
  dc_prodtype_wrong,
  base_prodtype_wrong,
  missing_ic_core,
  missing_dc_core,
  base_description_issue
FROM issues
HAVING  (ic_prodtype_wrong +
   dc_prodtype_wrong +
   base_prodtype_wrong +
   missing_ic_core +
   missing_dc_core +
   base_description_issue) > 0
ORDER BY base_prod;
"""

